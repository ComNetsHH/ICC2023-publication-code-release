# Simple plot script for graph6 strings, take care of character escaping and node numbering when comparing to julia results
import networkx as nx
import matplotlib.pyplot as plt

graph_strings = [ b"Gfl}n_"]

for graph_string in graph_strings:
    G = nx.from_graph6_bytes(graph_string)
    nx.draw(G, with_labels=True)
    plt.show()